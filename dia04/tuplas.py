#%%
# Uma tupla em Python é uma sequência ordenada de elementos que não pode ser alterada após sua criação. Isso significa que uma vez que você cria uma tupla, não pode adicionar, remover ou modificar elementos dentro dela.

#Ou seja, é tipo uma "lista imutável"

nomes = ('teo', 'nah', 'maria')
print(nomes)
#%%

nomes[::-1] #tupla NÃO ALTERA! Reordenou mas CRIOU UMA TUPLA NOVA!
